public class Count1to10 {
    public static void main(String [] args){
        for(int i=0; i<11; i++){
            System.out.println(i);
        }
        // for(int j=0; j<11; j++);{
        //     System.out.println(j+" ");
        // }

        int k=0;
        while(k<11){
            System.out.println(k);
            k++;
        }
    }
}
